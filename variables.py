# Variables and Data Types
name = "Javier"
age = 30
height = 5.9
is_programmer = True
print(name, "is", age, "years old and", height, "feet tall.")
print("Is", name, "a programmer?", is_programmer)

# Basic Operations
a = 10
b = 3
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)
print("a ** b =", a ** b)

# If Statements
temperature = 20
if temperature > 30:
    print("It's a hot day.")
elif temperature > 20:
    print("It's a nice day.")
else:
    print("It's cold.")
